int parse(unsigned char* inputData){
	unsigned int counter = 0;
	unsigned short cipher = 0x0;
	for (int i=0; i < KEY_SIZE ; i++){
			cipher = (((short)inputData[counter]) << 8) | inputData[counter+1];
			cipher ^= 0x1337;
			if (cipher != data[i]){
				return 1;
			}
			counter +=2 ;
	}
	return 0;
}

